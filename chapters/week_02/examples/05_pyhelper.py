print("=" * 40)
print()

# 新增：询问用户心情
print("今天心情怎么样？")
print("1. 充满干劲")
print("2. 一般般")
print("3. 有点累")

mood = input("\n请输入你的心情（1-3）：")

# 新增：根据心情给出不同建议（if/elif/else）
if mood == "1":
    print("\n太好了！推荐你今天挑战一个新概念，")
    print("比如开始学习函数或者列表。")
elif mood == "2":
    print("\n那就做点巩固练习吧，")
    print("复习上周的变量和字符串，写几个小例子。")
elif mood == "3":
    print("\n累了就休息一下吧，")
    print("今天可以只看视频不动手，或者写 10 分钟代码就停。")
else:
    print("\n输入无效？那就按'一般般'来吧，")
    print("写点巩固练习最稳妥。")

print()
