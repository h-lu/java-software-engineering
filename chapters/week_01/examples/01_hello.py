from __future__ import annotations


# 使用 print() 函数在屏幕上输出文字
# 文字需要用引号包裹（单引号或双引号都可以）
print("Hello, World!")

# 也可以输出中文
print("你好，Python！")

# 可以输出多行文字
print("第一行")
print("第二行")
