# 程序说明注释
# 作者：学习者
# 功能：交互式个人名片生成器
# 版本：Week 01 最终版

print("=" * 40)
print("        个人名片生成器 v1.0")
print("=" * 40)
print()

# --- 获取用户输入 ---
# 注意：input() 返回的永远是字符串，即使输入的是数字
name = input("请输入姓名：")
job = input("请输入职业：")
email = input("请输入邮箱：")

# --- 潜在错误点 1：忘记输入内容 ---
# 如果用户直接按回车，变量会是空字符串 ""
# 后续版本可以添加输入验证

# --- 生成名片 ---
# 使用 f-string 格式化输出
# 注意：大括号 {} 要成对出现，否则会导致 SyntaxError

print("\n" + "=" * 40)
print(f"姓名：{name}")
print(f"职业：{job}")
print(f"邮箱：{email}")
print("=" * 40)

# --- 潜在错误点 2：变量名拼写错误 ---
# 如果上面写的是 name，这里写成 Name，会报 NameError
# 建议：始终使用小写字母，多个单词用下划线分隔

print("\n名片生成完成！")

